
public class Leapyear {
	public static void main(String[] args) {
		// when the year is divisible by 4 then it is a leap year
		
		int a = 2028;
		
		if (a%4 == 0) {
			System.out.println("It is an leap year");
		}
		else {
			System.out.println("It is not an leap year");
		}
	}

}
